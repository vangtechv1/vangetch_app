                    Text(
                      "Already have an account? ",
                      style: TextStyle(
                        color: Colors.black87.withOpacity(0.7),
                      ),
                    ),
                    TextButton(
                      onPressed: () {
                        Navigator.push(
                          context,
                          MaterialPageRoute(
